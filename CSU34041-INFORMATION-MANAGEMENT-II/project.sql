DROP DATABASE csgoStats;
CREATE DATABASE csgoStats;
use csgoStats;
CREATE TABLE TEAM(
	TEAM_ID Integer NOT NULL UNIQUE,
    TEAM_NAME VARCHAR(255) NOT NULL,
    LOCATION VARCHAR(255) NOT NULL,
    REGION VARCHAR(255) NOT NULL,
    START_DATE DATE NOT NULL,
    DISBAND DATE,
    RUNNING_PERIOD INT,
    TOTAL_WINNING Integer NOT NULL,
    PRIMARY KEY (TEAM_ID)

);
CREATE TABLE SPONSOR(
	SPONSOR_ID INTEGER NOT NULL UNiQUE,
    SPONSOR_NAME VARCHAR(255) NOT NULL,
    SPONSOR_LINK VARCHAR(255) NOT NULL,
    PRIMARY KEY (SPONSOR_ID)
);
CREATE TABLE SPONSOR_TABLE(
	TEAM_ID INTEGER NOT NULL,
    SPONSOR_ID INTEGER NOT NULL,
    FOREIGN KEY (TEAM_ID) REFERENCES TEAM(TEAM_ID),
    FOREIGN KEY (SPONSOR_ID) REFERENCES SPONSOR(SPONSOR_ID),
    PRIMARY KEY(TEAM_ID,SPONSOR_ID)
);
CREATE TABLE PLAYER(
	PLAYER_ID INTEGER NOT NULL UNIQUE,
    TEAM_ID INTEGER NOT NULL,
    PLAYER_ROLE VARCHAR(255),
	NICKNAME VARCHAR(255) NOT NULL,
    FORENAME VARCHAR(255) NOT NULL,
	SURNAME VARCHAR(255) NOT NULL,
    AGE INTEGER NOT NULL,
    NATIONALITY VARCHAR(255) NOT NULL,
    JOIN_TEAM DATE NOT NULL,
    QUIT_TEAM DATE,
    ONBOARD INTEGER,
    PRIMARY KEY (PLAYER_ID),
    FOREIGN KEY (TEAM_ID) REFERENCES TEAM(TEAM_ID),
    CONSTRAINT CHECK (PLAYER_ROLE IN ('IGL','Entry Fragger','Lurker','Supporter','AWPER'))
);
CREATE TABLE MAP(
	MAP_ID INTEGER NOT NULL UNIQUE,
    MAP_NAME VARCHAR(255) NOT NULL,
    TERROIST VARCHAR(255) NOT NULL,
    COUNTER_TERROIST VARCHAR(255) NOT NULL,
    LOCATION VARCHAR(255) NOT NULL,
    THEME VARCHAR(255) NOT NULL,
    PRIMARY KEY(MAP_ID)
);
CREATE TABLE MAP_POOL(
	MAP_POOL_ID INTEGER NOT NULL,
    MAP1 INTEGER NOT NULL,
    MAP2 INTEGER NOT NULL,
    MAP3 INTEGER NOT NULL,
    MAP4 INTEGER NOT NULL,
    MAP5 INTEGER NOT NULL,
    MAP6 INTEGER NOT NULL,
    MAP7 INTEGER NOT NULL,
    PRIMARY KEY(MAP_POOL_ID),
	FOREIGN KEY (MAP1) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP2) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP3) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP4) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP5) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP6) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MAP7) REFERENCES MAP(MAP_ID),
    CHECK (MAP1 != MAP2),CHECK (MAP1 != MAP3),CHECK (MAP1 != MAP4),CHECK (MAP1 != MAP5),CHECK (MAP1 != MAP6),CHECK (MAP1 != MAP7),
    CHECK (MAP2 != MAP3),CHECK (MAP2 != MAP4),CHECK (MAP2 != MAP5),CHECK (MAP2 != MAP6),CHECK (MAP2 != MAP7),
    CHECK (MAP3 != MAP4),CHECK (MAP3 != MAP5),CHECK (MAP3 != MAP6),CHECK (MAP3 != MAP7),
    CHECK (MAP4 != MAP5),CHECK (MAP4 != MAP6),CHECK (MAP4 != MAP7),
    CHECK (MAP5 != MAP6),CHECK (MAP5 != MAP6),
    CHECK (MAP6 != MAP7)
);
CREATE TABLE TOURNAMENT(
	TOURNAMENT_ID INTEGER NOT NULL UNIQUE,
    TOURNAMENT_NAME VARCHAR(255) NOT NULL,
    START_DATE DATE NOT NULL,
    END_DATE DATE NOT NULL,
    PRIZE_POOL INTEGER NOT NULL,
    LOCATION VARCHAR(255) NOT NULL,
    CHAMPION INTEGER,
    MVP_PLAYER INTEGER,
    MAP_POOL INTEGER NOT NULL,
    CHECK (START_DATE <= END_DATE),
    PRIMARY KEY (TOURNAMENT_ID),
    FOREIGN KEY (CHAMPION) REFERENCES TEAM(TEAM_ID),
    FOREIGN KEY (MVP_PLAYER) REFERENCES PLAYER (PLAYER_ID),
    FOREIGN KEY (MAP_POOL) REFERENCES MAP_POOL (MAP_POOL_ID)
);
CREATE TABLE TEAM_ATTENDING(
	TOURNAMENT_ID INTEGER NOT NULL,
    TEAM_ID INTEGER NOT NULL,
    FOREIGN KEY (TOURNAMENT_ID) REFERENCES TOURNAMENT(TOURNAMENT_ID),
    FOREIGN KEY (TEAM_ID) REFERENCES TEAM(TEAM_ID)
);
CREATE TABLE COMPETITION(
	TOURNAMENT_ID INTEGER NOT NULL,
    COMPETITION_ID INTEGER NOT NULL UNIQUE,
    TEAM_A INTEGER NOT NULL,
    TEAM_B INTEGER NOT NULL,
    COMPETITION_WINNER INTEGER,
    COMPETITION_SCORE VARCHAR(255),
    FOREIGN KEY (TEAM_A) REFERENCES TEAM(TEAM_ID),
    FOREIGN KEY (TEAM_B) REFERENCES TEAM(TEAM_ID),
	FOREIGN KEY (COMPETITION_WINNER) REFERENCES TEAM(TEAM_ID),
    PRIMARY KEY (COMPETITION_ID)
);
CREATE TABLE MATCH_DETAIL(
	TOURNAMENT_ID INTEGER NOT NULL,
    COMPETITION_ID INTEGER NOT NULL,
	MATCH_ID INTEGER NOT NULL UNIQUE,
    MATCH_WINNER INTEGER NOT NULL,
    MATCH_SCORE VARCHAR(255) NOT NULL,
    MAP_PLAYED INTEGER NOT NULL NOT NULL,
    MOST_KILLS INTEGER NOT NULL,
    MOST_DAMAGE INTEGER NOT NULL,
    MOST_ASSITANT INTEGER NOT NULL,
    MOST_AWP_KILLS INTEGER NOT NULL,
    MOST_FIRST_KILLS INTEGER NOT NULL,
    BEST_RATING INTEGER NOT NULL,
    FOREIGN KEY (MATCH_WINNER) REFERENCES TEAM(TEAM_ID),
    FOREIGN KEY (MAP_PLAYED) REFERENCES MAP(MAP_ID),
    FOREIGN KEY (MOST_KILLS) REFERENCES PLAYER(PLAYER_ID),
    FOREIGN KEY (MOST_DAMAGE) REFERENCES PLAYER(PLAYER_ID),
    FOREIGN KEY (MOST_ASSITANT) REFERENCES PLAYER(PLAYER_ID),
    FOREIGN KEY (MOST_AWP_KILLS) REFERENCES PLAYER(PLAYER_ID),
    FOREIGN KEY (MOST_FIRST_KILLS) REFERENCES PLAYER(PLAYER_ID),
    FOREIGN KEY (BEST_RATING) REFERENCES PLAYER(PLAYER_ID),
    PRIMARY KEY (MATCH_ID)
);


CREATE TRIGGER CALCULATE_RUNNING_PERIOD 
BEFORE UPDATE ON TEAM  
FOR EACH ROW
	SET NEW.RUNNING_PERIOD = TIMESTAMPDIFF(DAY,OLD.START_DATE,NEW.DISBAND);
    
CREATE TRIGGER CALCULATE_PLAYER_ONBOARD
BEFORE UPDATE ON PLAYER
FOR EACH ROW 
	SET NEW.ONBOARD = TIMESTAMPDIFF(DAY,OLD.JOIN_TEAM,NEW.QUIT_TEAM);
    
    
    
